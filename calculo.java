package ListaLucas;

public class calculo {
		
    public int soma(int num1, int num2) {
    	
    int soma = num1 + num2;
    
		return soma;
		
	}
    
    public int menos(int num1, int num2) {
    	
        int menos = num1 - num2;
        
    		return menos;
    		
    	}
    
    public int multi(int num1, int num2) {
    	
        int multi = num1 * num2;
        
    		return multi;
    		
    	}
    
    public int div(int num1, int num2) {
    	
        int div = num1 / num2;
        
    		return div;
    		
    	}
   
}
